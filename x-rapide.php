              <h4>Design Solution</h4>
        </div>
        <div class="side-para">
          <p>Based on our research findings, we designed the Rapide KDS application with a clean, intuitive UI that allows users to quickly and easily manage orders. The UI is designed with high-contrast colours and large, easy-to-read fonts to make it easier for kitchen staff to view and understand order details. We also designed the UI to be responsive for different devices with different screen sizes.</p>
          <p>The Rapide KDS interface is organized into three main sections: the order list, order details, and preparation status. The order list displays all active orders in a clear, easy-to-read format. The order details section provides additional information about each order, such as customer name, table number, and order items. Finally, the preparation status section allows kitchen staff to update the status of each order, from "new order" to "processing" to "ready."</p>
          <p>To help users stay organized and focused, the application features color-coded order statuses corresponding to each stage of order preparation. This allows kitchen staff to quickly identify orders that need attention and prioritize their work accordingly. To simplify order management, we designed the application to integrate with popular POS systems, allowing orders to be automatically transmitted from the POS to the KDS. This eliminates the need for manual order entry and reduces the risk of errors.</p>

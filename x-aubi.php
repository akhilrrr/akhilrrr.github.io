            <div class="aubi-uresear">
              <ul>
                <li><p><b><u>Demographics:</u></b> The survey included a mix of male and female participants from different age groups, with the majority being between the ages of 25-45. Most participants (70%) were frequent travellers and used smartwatches.</p></li>
                <li><p><b><u>Travel Habits:</u></b> Most participants (80%) reported travelling for leisure, while 20% travelled for business. The top travel destinations were Asia, Europe, and North America. The average length of a trip was 7-10 days.</p></li>
                <li><p><b><u>Language Usage:</u></b> The survey found that the most commonly spoken languages were English, Chinese, Spanish, French, and German. Participants reported that they often needed help understanding signs, menus, and conversations in foreign languages.</p></li>
                <li><p><b><u>Translation Needs:</u></b> The survey found that most participants (90%) would use a real-time translation application if it were available on their smartwatch. Participants preferred an application that could translate both written and spoken language.</p></li>
                <li><p><b><u>Features:</u></b> The most requested features were accurate translation, real-time translation, and the ability to translate multiple languages. Participants also expressed a preference for a simple and intuitive interface.</p></li>
                <li><p><b><u>User Experience:</u></b> Participants indicated that they would like an application that is easy to use, with clear instructions and guidance. They also preferred an application that was customizable to their needs and preferences.</p></li>
                <li><p><b><u>Design Preferences:</u></b> Participants expressed a preference for a clean and modern design, with a focus on functionality over aesthetics. They also indicated a preference for a dark mode option and the ability to customize the interface.</p></li>
                <li><p><b><u>Privacy and Security:</u></b> Participants expressed concerns about the privacy and security of their personal information, mainly when using a real-time translation application. They preferred a transparent application regarding its data collection and storage practices.</p></li>
                <li><p><b><u>Integration with Other Applications:</u></b> Participants desired the real-time translation application to integrate with other travel-related applications, such as maps, transportation, and booking services.</p></li>
                <li><p><b><u>Price:</u></b>  Participants indicated they would be willing to pay for a real-time translation application, but the price should be reasonable and competitive with similar applications.</p></li>
